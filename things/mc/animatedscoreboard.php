                <div class="sections">
                    <div class="section">
                        <h2 class="btn-section" data-section="section1">What is AnimatedScoreboard? <i class="fa-solid fa-chevron-down"></i></h2>
                        <h4 id="section1" style="display: none;">AnimatedScoreboard is a plugin used to add a scoreboard to users. AnimatedScoreboard has a feature to add a scoreboard for users of specific ranges. AnimatedScoreboard has an animation system so that your scoreboard looks better. You can also add placeholders with the placeholderapi plugin, these are contents that interact with the player or the server. AnimatedScoreboard has commands to reset the scoreboard and add plugin updates without having to update the scoreboard.</h4>
                    </div>
                    <div class="section">
                        <h2 class="btn-section" data-section="section2">Description of AnimatedScoreboard configured <i class="fa-solid fa-chevron-down"></i></h2>
                        <h4 id="section2" style="display: none;">AnimatedScoreboard pre-configured has several placeholders. The title has a beautiful animation. Then features color coded minecraft creator colors. The scoreboard is full of emojis inside Minecraft (those emojis are not put with a plugin). The scoreboard has 11 lines, there is a 4-line paragraph and two 2-line paragraphs. This to differentiate the player's things. The first paragraph would be the player information. The second paragraph is useful server information and the last paragraph is player statistics. The content of the scoreboard adapts automatically, that means that each player sees on the scoreboard depending on his information and statistics. Currently the language of the scoreboard is English, but you can modify the scoreboard however you want from the defaultscoreboard.yml file.</h4>
                    </div>
                    <div class="section">
                        <h2 class="btn-section" data-section="section3">How to download <i class="fa-solid fa-chevron-down"></i></h2>
                        <h4 id="section3" style="display: none;">Download the file and unarchive it. Download the plugin and start the server. Then go to your server files, go to the folder named plugins and delete the old AnimatedScoreboard folder and add the new one. It is your decision to download some plugins to put more APIs and make your scoreboard more dynamic and more useful on your server.</h4>
                    </div>
