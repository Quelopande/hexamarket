                <div class="sections">
                    <div class="section">
                        <h2 class="btn-section" data-section="section1">What is Luckperms? <i class="fa-solid fa-chevron-down"></i></h2>
                        <h4 id="section1" style="display: none;">Luckperms is a Minecraft plugin used mainly for making ranges. These ranges usually have prefixes and suffixes, these can have colors. Luckperms supports setting permissions to ranges so that they can perform some actions within the server. Luckperms allows you to set specific ranks to only some players, and also allows you to set a permission to a specific player without changing the settings of a rank. The Luckperms plugin is featured by the web editor where it lets you edit ranks faster and through a browser outside of Minecraft.</h4>
                    </div>
                    <div class="section">
                        <h2 class="btn-section" data-section="section2">Description of Luckperms pre-configured <i class="fa-solid fa-chevron-down"></i></h2>
                        <h4 id="section2" style="display: none;">Luckperms pre-configured has eleven ranks with custom prefixes and diamond colors to make them stand out in chat. Paid ranks and admin ranks have chat grayed out to make them stand out, but regular users have chat grayed out so it doesn't stand out, making it look more professional and more beneficial to the servers. All ranks have permissions to access the kits of their respective rank and all the other commands that survival custom or pvp usually have (the kits will have to be created by you or installed. Most of the permissions are from the essentials, download it here: <a href="https://www.hexamarket.store/things/mc/essentialsx"> EssentialsX pre-configured</a>) , admin members have permission to run some commands to moderate your server well , there are 5 server admin ranks, 5 server donor ranks and 1 user rank.</h4>
                    </div>
                    <div class="section">
                        <h2 class="btn-section" data-section="section3">How to install <i class="fa-solid fa-chevron-down"></i></h2>
                        <h4 id="section3" style="display: none;">Download the file and unarchive it. Download the Luckperms plugin and start the server. Next, go to your server files, go to the folder named plugins and delete the old Luckperms folder and add what you got earlier after unarchiving the file. It is your decision to decide whether to put some plugins so that in the chat and in the tab you can see the prefixes and suffixes of the ranks. We recommend using Essentials Chat and Nametagedit.</h4>
                    </div>
