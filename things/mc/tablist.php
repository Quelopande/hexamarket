                <div class="sections">
                    <div class="section">
                        <h2 class="btn-section" data-section="section1">What is tablist? <i class="fa-solid fa-chevron-down"></i></h2>
                        <h4 id="section1" style="display: none;">Tablist is a plugin for Minecraft that allows server administrators to customize the appearance of the server’s tabs. It can be used to hide certain players and display player ranks. The plugin also includes placeholders to make the Tablist more functional.</h4>
                    </div>
                    <div class="section">
                        <h2 class="btn-section" data-section="section2">Description of Tablist pre-configured <i class="fa-solid fa-chevron-down"></i></h2>
                        <h4 id="section2" style="display: none;">Tablist is a very easy plugin to configure, but sometimes the design does not convince us and we have to repeat it over and over again until we reach a beautiful design and that it does not occupy the entire screen so that it does not stress the players and does not diminish the player's range of vision. Here you will find a Tablist with an animation and 2 placeholders. It has a beautiful design and also it does not take up so much of the screen so that players can see what is happening in the game well. The ranges are seen thanks to the Nametagedit plugin.</h4>
                    </div>
                    <div class="section">
                        <h2 class="btn-section" data-section="section3">How to install <i class="fa-solid fa-chevron-down"></i></h2>
                        <h4 id="section3" style="display: none;">Download the file and unarchive it. Download the plugin and start the server. Then go to your server files, enter the folder named plugins and delete the old Tablist folder and add the new one. It is your decision to download some plugins to put more APIs and make your tablist more professional, remember to configure Nametagedit if you have any rank system installed on your server</h4>
                    </div>
