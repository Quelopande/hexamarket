                        <h2 class="btn-section" data-section="section1">What is EssentialsX? <i class="fa-solid fa-chevron-down"></i></h2>
                        <h4 id="section1" style="display: none;">EssentialsX is a widely used plugin on many Minecraft servers. EssentialsX has multiple administration commands, these are to kick players from the server, ban a player from entering the server permanently or for a defined time, ban people with the same IP from entering the server and view a player's inventory or modify it. EssentialsX has a private message system, this means that you can send messages to other players, to the administration and send emails to other players. EssentialsX has teleportation commands, these are customized so that they are not abusive or annoying, in this category the most used command is the tpa, which is used to ask the other player if the claiming player can teleport to him, the requested player can accept and deny the teleportation request. EssentialsX is color coded to put them on signals and in chat. EssentialsX has an economy system that can be modifiable, change the server's currency symbol, change the maximum amount of money for a player and also an exact amount of money can be given to players who enter the server for the first time. . EssentialsX has a system to change the welcome messages. EssentialsX allows kitting with a delay.</h4>
                    </div>
                    <div class="section">
                        <h2 class="btn-section" data-section="section2">Description of EssentialsX pre-configured <i class="fa-solid fa-chevron-down"></i></h2>
                        <h4 id="section2" style="display: none;">EssentialsX pre-configured has a large variety of modified functions that are always useful, after all, all servers usually activate and deactivate it so that the server has a more secure environment. In the case of EssentialsX pre-configured the options are already configured to make the environment more secure, there are also options activated so that players can do some actions to improve their experience and make the server look more professional and fun. The language in which the plugin is in is Spanish. The plugin has a personalized welcome and farewell message to make his presence more noticeable on the chat and to make it look more professional. It also has the essentials chat already preconfigured with a nice design.</h4>
                    </div>
