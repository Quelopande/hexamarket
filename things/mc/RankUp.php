                        <h2 class="btn-section" data-section="section1">What is RankUp? <i class="fa-solid fa-chevron-down"></i></h2>
                        <h4 id="section1" style="display: none;">Rankup is a plugin that serves to give a reward to server users. This plugin is usually added to servers to encourage users to stay on the server in exchange for ranks which will then give you rewards or social status on the server. It also serves to entice users to purchase a rank on the server in exchange for real money. The Rankup system is basic, in exchange for objects, money and other types of elements from the server you obtain a rank. Then there will be a higher rank that will require something more to achieve. Ranks are usually from Luckperms or PowerRanks.</h4>
                    </div>
                    <div class="section">
                        <h2 class="btn-section" data-section="section2">Description of RankUp configured <i class="fa-solid fa-chevron-down"></i></h2>
                        <h4 id="section2" style="display: none;">Rankup configured is the folder with already configured and modified files of the RankUp plugin. The configuration is only added in the English translation file of the plugin. Messages are configured with custom gradients and colors. This will make the server more colorful when users use Rankup. The colorful messages follow a gradient pattern. Error messages range from orange to red. The rank up ones are from light pink to dark pink.</h4>
                    </div>
                    <div class="section">
                        <h2 class="btn-section" data-section="section3">How to install <i class="fa-solid fa-chevron-down"></i></h2>
                        <h4 id="section3" style="display: none;">Download the plugin file on the official Rankup website. Put the file in the plugins folder on your server. Then start the server so that the plugin creates a folder. When the plugin folder has been created, turn off the server and delete the folder. Then unarchive the Zip file that you get by clicking the download button just below. Copy the folder and paste it into the plugins folder on your server.</h4>
