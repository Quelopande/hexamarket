                        <h2 class="btn-section" data-section="section1">What is DeathMessage? <i class="fa-solid fa-chevron-down"></i></h2>
                        <h4 id="section1" style="display: none;">DeathMessage is a simple plugin that serves to inform users how they or another user died. This plugin is usually installed for aesthetic, informative purposes or simply to put a funny message for the deaths. It can also be used to misinform, that is, someone can say that someone has died in lava and that will be shown, but the user died in another way. This plugin also serves to welcome new, returning or other users. And to say goodbye to users who left the server because they wanted to or because an administrator expelled them.</h4>
                    </div>
                    <div class="section">
                        <h2 class="btn-section" data-section="section2">Description of DeathMessage configured <i class="fa-solid fa-chevron-down"></i></h2>
                        <h4 id="section2" style="display: none;">Configured DeathMessage is the same plugin as always, but customized for an aesthetic and informative purpose. The messages are with gradients with colors that describe the death of the user (e.g. You die of cold: the color goes from white to blue). The welcome messages are also personalized. When you enter, the first thing that appears in the message is [+] or [-] depending on whether you have entered or exited. The user's name appears in yellow to highlight the name from the rest of the gray message.</h4>
                    </div>
                    <div class="section">
                        <h2 class="btn-section" data-section="section3">How to install <i class="fa-solid fa-chevron-down"></i></h2>
                        <h4 id="section3" style="display: none;">Download the DeathMessage file at the link below. Put the file in the plugins section. Then start the server. When it is completely started, turn it off and go to the plugins folder on your server and delete the folder. Then go and download the file by clicking the download button below. Unarchive the Zip file it gives you and the folder you get, put it in the plugins folder and start the server again to check that everything is correct.</h4>
