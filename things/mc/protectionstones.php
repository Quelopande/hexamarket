                <div class="sections">
                    <div class="section">
                        <h2 class="btn-section" data-section="section1">What is ProtectionStones? <i class="fa-solid fa-chevron-down"></i></h2>
                        <h4 id="section1" style="display: none;">Protectionstones is a Minecraft plugin widely used on all servers. Protectionstones allow users to protect their plots so that they feel more secure. Protectionstones have a flag system, that means you can set up a specific protection to block damage, break things and much more within the plots. This plugin also has an economy system to make protections worth money.</h4>
                    </div>
                    <div class="section">
                        <h2 class="btn-section" data-section="section2">Description of Protectionstones pre-configured <i class="fa-solid fa-chevron-down"></i></h2>
                        <h4 id="section2" style="display: none;">Protectionstones pre-configured has a basic flag system that allows security on the plot not to be abusively secure (no blows...) nor unprotected (it cannot be broken). When entering a plot, a customized and highlighted message appears to notify users outside of them that they are entering another plot that is not theirs. The same thing happens when leaving, the only difference is that it notifies you when they are leaving it. It also has an economy system that only charges you when you buy the protection. There are 5 different protections all have different price, range of protection and 'ore'</h4> 
                    </div>
                    <div class="section">
                        <h2 class="btn-section" data-section="section3">How to install <i class="fa-solid fa-chevron-down"></i></h2>
                        <h4 id="section3" style="display: none;">Download the file and unarchive it. Download the plugin protectionstones, world edit and worldguard and start the server. Then go to your server files, go to the folder named plugins and delete the old protectionstones folder and add the new one. It is your decision to download some plugins to put more of an economy system (it comes by default with protectionstones pre-configured) and that the protections are more professional. Remember to install an economy system plugin so that the plugin runs smoothly.</h4>
                    </div>
