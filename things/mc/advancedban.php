                <div class="sections">
                    <div class="section">
                        <h2 class="btn-section" data-section="section1">What is AdvancedBan? <i class="fa-solid fa-chevron-down"></i></h2>
                        <h4 id="section1" style="display: none;">AdvancedBan is a Minecraft plugin that allows server administrators to customize penalties and bans. This plugin offers a wide range of features, including the ability to ban players for a set time, the ability to issue warnings, and the option to customize ban messages. There is also a translated version of the plugin.</h4>
                    </div>
                    <div class="section">
                        <h2 class="btn-section" data-section="section2">Description of AdvancedBan configured <i class="fa-solid fa-chevron-down"></i></h2>
                        <h4 id="section2" style="display: none;">AdvancedBan configured contains a variety of custom messages for when you ban a user on your server. The messages are composed of colors in a gradient so that they look better visually and the server obtains a serious appearance in the eyes of users. The messages that are seen in the chat of the sanctioned person when you silence him or add some type of infraction have all kinds of information and are customized so that the user can distinguish between common messages from the server and warnings. The sanctions screens that appear when you ban or ban someone are full of information necessary for the sanctioned user. These screens contain the website and the discord where you can appeal the sanction, obviously this can be changed or removed through the “Messages.yml” file so that in this way only the name of the moderator who has sanctioned or expelled you remains, the type of sanction you have obtained, the reason for the sanction and the time remaining for the sanction to be removed if it is temporary.</h4>
                    </div>
                    <div class="section">
                        <h2 class="btn-section" data-section="section3">How to install <i class="fa-solid fa-chevron-down"></i></h2>
                        <h4 id="section3" style="display: none;">Download the AdvancedBan file. Put the file in the plugins folder on your server. Then start the server. Go to the plugins folder and there you will see a newly created AdvancedBan folder. Delete the “AdvancedBan” folder and replace it with the folder you get when you unarchive the “AdvancedBan.zip” file. Start the server again and everything would be configured. You can edit the messages in the “Messages.yml” file that is inside the AdvancedBan folder.</h4>
                    </div>
