                    <div class="section">
                        <h2 class="btn-section" data-section="section2">Description of Economyshopgui pre-configured <i class="fa-solid fa-chevron-down"></i></h2>
                        <h4 id="section2" style="display: none;">Unfortunately, all of us Minecraft configurators know how difficult it is to configure the economyshopgui plugin since the tutorials that are available through photos on the internet are not well explained due to how complex it is to configure it. Changing the sale and purchase prices is the most difficult to set up. Prices so cheap and with a high asking price are some of the things that come by default are some of the things that make the task of setting up difficult for most set ups. Here we bring you Economyshopgui pre-configured, it is the economyshopgui rearranged and with well-regulated prices so that your server has a very good economy, and that your server has a more professional and balanced environment.</h4>
                    </div>
                    <div class="section">
                        <h2 class="btn-section" data-section="section3">How to install <i class="fa-solid fa-chevron-down"></i></h2>
                        <h4 id="section3" style="display: none;">Download the economyshopgui plugin (link below), install Vault (link below) and install your economy system and configure it (Essentials X does not require configuration). Go to your server files, enter the plugins folder and put all the files you downloaded before there. Start the server, go back to the plugins folder and delete the old economyshopgui folder. Unarchive the .zip file. Place the new folder inside the plugins section. Start the server and check that everything is ok.</h4>
