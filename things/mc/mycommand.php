                    <div class="section">
                        <h2 class="btn-section" data-section="section1">What is Mycommand? <i class="fa-solid fa-chevron-down"></i></h2>
                        <h4 id="section1" style="display: none;">Mycommand is a Minecraft plugin that does many things, but it stands out for having a feature that sets permissions for commands. Mycommand is also notable because you can add an alias to the command, meaning it changes a command but the response doesn't change. It also serves to make your own command and can be executed from the player or from the console. Mycommand has a system of texts in the air (like holographic displays). It also has a custom and animated menu system for your server. Mycommand also has a scoreboard system.</h4>
                    </div>
                    <div class="section">
                        <h2 class="btn-section" data-section="section2">Description of the Mycommand pre-configured <i class="fa-solid fa-chevron-down"></i></h2>
                        <h4 id="section2" style="display: none;">Mycommand pre-configured has a large variety of commands blocked so that users without permission can know some server data (for example: plugins that the server has, version ...) that administrators do not want them to know. It also has the command abbreviations of other plugins (for example: command panels, EssentialsX …). Mycommand pre-configured has sample commands so you can create your own commands by following the examples.</h4>
                    </div>
                    <div class="section">
                        <h2 class="btn-section" data-section="section3">How to install <i class="fa-solid fa-chevron-down"></i></h2>
                        <h4 id="section3" style="display: none;">Download the file and unarchive it. Download the plugin and start the server. Then go to your server files, go to the folder named plugins and delete the old Mycommand folder and add the new one. It is your decision to download some plugins to put more apis and make your commands more professional and useful.</h4>
                    </div>
