                    <div class="section">
                        <h2 class="btn-section" data-section="section1">What is CommandPanels? <i class="fa-solid fa-chevron-down"></i></h2>
                        <h4 id="section1" style="display: none;">CommandPanels is a Minecraft plugin that allows you to create custom interactive inventories. You can use variables, data, and placeholders to customize your GUIs. Inventories can be animated and change items constantly. You can even make a store with this plugin!</h4>
                    </div>
                    <div class="section">
                        <h2 class="btn-section" data-section="section2">Description of CommandPanels configured <i class="fa-solid fa-chevron-down"></i></h2>
                        <h4 id="section2" style="display: none;">CommandPanels configured has 4 menus full of colors and logos, things and objects that makes it more beautiful. Most of the items in the menus are ToolTips (Hovering over shows information). But there are also other elements that will redirect you to another menu, but also when you click on some specific objects it will show you informative messages about the server in the server chat, these will only be shown to the person who clicked on it, we recommend that you change the information to these informative elements. There is a stats menu, a kits menu that is made for running the essentialsX /kit {kit name} command. But this is not all! There is also a menu that shows you the stats of the player who opened the menu.</h4>
                    </div>
                    <div class="section">
                        <h2 class="btn-section" data-section="section3">How to install<i class="fa-solid fa-chevron-down"></i></h2>
                        <h4 id="section3" style="display: none;">Download the file and unarchive it. Download the Luckperms plugin, put it in the plugins folder of your server and start the server. Next, go to your server files, go to the folder named plugins and delete the old Luckperms folder and add what you obtained previously after unarchiveing the file. It's your decision to decide whether to put some as PlaceholderApi so that the menus work properly. We also recommend that you put EssentialsX or some type of kits plugin on your server for the kits menu to work, but first you must change the menu content in the file inside the "server/plugins/Panels" folder and go to kits.yml to edit this menu command. We recommend that you do the same with the main menu messages (the file is called "panel-1.yml").</h4>
