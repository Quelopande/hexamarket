                <div class="sections">
                    <div class="section">
                        <h2 class="btn-section" data-section="section1">¿Qué es Protectionsotones? <i class="fa-solid fa-chevron-down"></i></h2>
                        <h4 id="section1" style="display: none;">Protectionstones es un plugin de Minecraft muy usado en todos los servidores. Protectionstones permite a los usuarios proteger sus parcelas para que se sientas más seguros. Protectionstones tiene un sistema de flags, eso significa que puedes configurar una protección concreta para bloquear el daño, romper cosas y muchas cosas más dentro de las parcelas. Este plugin cuenta también con un sistema de economía para hacer que las protecciones valgan dinero.</h4>
                    </div>
                    <div class="section">
                        <h2 class="btn-section" data-section="section2">Descripción de Protectionstones configurado <i class="fa-solid fa-chevron-down"></i></h2>
                        <h4 id="section2" style="display: none;">Protectionstones pre-configured tiene un sistema de flags básico que permite que la seguridad en la parcela no sea abusivamente segura (sin golpes…) ni tampoco desprotegida (que no se pueda romper). Al entrar a una parcela aparece un mensaje customizado y resaltado para notificar a usuarios ajenos a ellas que están entrando a otra parcela que no es suya. Pasa lo mismo al salir la única diferencia es que te avisa cuando están saliendo de ella. También cuenta con un sistema de economía solo te cobra al comprar la protección. Hay 5 diferentes protecciones todas tienen diferente precio, rango de protección y ‘ore’ (mineral).</h4>
                    </div>
                    <div class="section">
                        <h2 class="btn-section" data-section="section3">Como instalarlo <i class="fa-solid fa-chevron-down"></i></h2>
                        <h4 id="section3" style="display: none;">Descárgate el fichero y desarchive lo. Descargase el plugin protectionstones, world edit y worldguard e inicie el server. A continuación, diríjase a los ficheros de su servidor, entre a la carpeta con el nombre plugins y elimine la carpeta antigua de protectionstones y agregue la nueva. Es su decisión descargar algunos plugins para poner más un sistema de economía (viene de default con protectionstones pre-configured) y que las protecciones sean más profesionales. Recuerde instalar un plugin de sistema de economía para que el plugin vaya fluido.</h4>
                    </div>
