                        <h2 class="btn-section" data-section="section1">¿Qué es EssentialsX? <i class="fa-solid fa-chevron-down"></i></h2>
                        <h4 id="section1" style="display: none;">EssentialsX es un plugin bastante usado en bastantes servidores de Minecraft. EssentialsX tiene múltiples comandos de administración, estos son para expulsar a jugadores del server, prohibir la entrada al server a un jugador permanente o por un tiempo definido, prohibir la entrada al server a personas con la misma IP y ver el inventario de un jugador o modificarlo. EssentialsX tiene un sistema de mensajes privados, esto significa que pueden enviar mensajes a otros jugadores, a la administración y enviar emails a otros jugadores. EssentialsX tiene comandos de tele transportación, estos están customizados para que no sean abusivos ni molestos, de esta categoría el comando más usado es el tpa, que sirve para pedirle al otro jugador si el jugador reclamante puede tele transportarse hacia él, el jugador solicitado puede aceptar y denegar la petición de tele transportación. EssentialsX tiene códigos de colores para ponerlos en señales y en el chat. EssentialsX tiene un sistema de economía este puede ser modificable, cambiar el símbolo de la divisa del servidor, cambiar la cantidad máxima de dinero de un jugador y también se puede dar una cantidad exacta de dinero a los jugadores que entren por primera vez a el servidor. EssentialsX tiene un sistema para cambiar los mensajes de bienvenida. EssentialsX permite hacer kits con un retraso.</h4>
                    </div>
