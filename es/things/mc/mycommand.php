                        <h2 class="btn-section" data-section="section1">¿Qué es Mycommand? <i class="fa-solid fa-chevron-down"></i></h2>
                        <h4 id="section1" style="display: none;">Mycommand es un plugin de Minecraft que sirve para muchas cosas, pero es destacado por tener una funcionan que les pone permisos a comandos. Mycommand también se destaca porque puede agregar un alias al comando, es decir que cambia un comando pero la respuesta no cambia. También sirve para hacer tu propio comando y se puede ejecutar desde el jugador o desde la consola. Mycommand cuenta con un sistema de textos en el aire (como holographic displays). También cuenta con un sistema de menús personalizados y animados para tu servidor. Mycommand cuenta también con un sistema de scoreboards.</h4>
                    </div>
                    <div class="section">
                        <h2 class="btn-section" data-section="section2">Descripción de Mycommand configurado <i class="fa-solid fa-chevron-down"></i></h2>
                        <h4 id="section2" style="display: none;">Mycommand pre-configured tiene una variedad grande de comandos bloqueados para que los usuarios sin permiso puedan conocer algunos datos del servidor (por ejemplo: plugins que tiene el server, versión …)  que los administradores no quieren que sepan. También cuenta con las acortaciones de comandos de otros plugins (por ejemplo: command panels, EssentialsX …). Mycommand pre-configured cuenta con comandos de ejemplo para que usted se pueda crear sus propios comandos siguiendo los ejemplos.</h4>
                    </div>
                    <div class="section">
                        <h2 class="btn-section" data-section="section3">Como instalarlo <i class="fa-solid fa-chevron-down"></i></h2>
                        <h4 id="section3" style="display: none;">Descárgate el fichero y desarchive lo. Descargase el plugin e inicie el server. A continuación, diríjase a los ficheros de su servidor, entre a la carpeta con el nombre plugins y elimine la carpeta antigua de Mycommand y agregue la nueva. Es su decisión descargar algunos plugins para poner más apis y que sus comandos sean más profesionales y que sean más utiles.</h4>
