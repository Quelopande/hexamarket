                    <div class="section">
                        <h2 class="btn-section" data-section="section1">¿Qué es Luckperms? <i class="fa-solid fa-chevron-down"></i></h2>
                        <h4 id="section1" style="display: none;">Luckperms es un plugin de Minecraft usado principalmente para hacer rangos. Estos rangos suelen tener prefijos y sufijos, estos pueden tener colores. Luckperms admite ponerle permisos a los rangos para que puedan ejecutar algunas acciones dentro del servidor. Luckperms deja ponerles rangos específicos a solo algunos jugadores, y también deja ponerle un permiso a un jugador en especial sin necesidad de cambiar la configuración de un rango. El plugin Luckperms es destacado por el web editor donde deja editar los rangos más rápido y a través de un navegador externo a Minecraft.</h4>
                    </div>
                    <div class="section">
                        <h2 class="btn-section" data-section="section2">Descripción de Luckperms configurado <i class="fa-solid fa-chevron-down"></i></h2>
                    <h4 id="section2" style="display: none;">Luckperms pre-configured tiene once rangos con prefijos customizados y con colores diamativos para que en el chat resalten. Los rangos de paga y los de los miembros de la administración tienen el chat de color gris para que resalten, pero los usuarios comunes tienen el chat en gris para que no resalte y se vea así más profesional y más beneficioso para los servidores. Todos los rangos tienen permisos, para acceder a los kits de su respectivo rango y todos los demás comandos que suelen tener los survival custom o pvp (los kits los tendrá que crearlos usted o instalarte. La mayoría de los permisos son del essentials descárgatelo aquí: <a href="https://www.menzatyx.xyz/es/things/mc/essentialsx">EssentialsX pre-configured</a>) , los miembros de la administración tienen permiso para ejecutar algunos comandos para moderar bien su servidor, hay 5 rangos de administradores del servidor, 5 de donadores del servidor y 1 rango de usuario.</h4>
                    </div>
                    <div class="section">
                        <h2 class="btn-section" data-section="section3">Como instalarlo <i class="fa-solid fa-chevron-down"></i></h2>
                        <h4 id="section3" style="display: none;">Descárgate el fichero y desarchive lo. Descargase el plugin Luckperms e inicie el server. A continuación, diríjase a los ficheros de su servidor, entre a la carpeta con el nombre plugins y elimine la carpeta antigua de Luckperms y agregue lo que obtuvo anteriormente después de desarchivar el fichero. Es su decisión decidir si poner algunos plugins para que en el chat y en el tabulador se vean los prefijos y sufijos de los rangos. Le recomendamos usar Essentials Chat y Nametagedit.</h4>
