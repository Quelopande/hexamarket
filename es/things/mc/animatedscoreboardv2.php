                    <div class="section">
                        <h2 class="btn-section" data-section="section1">¿Qué es AnimatedScoreboard? <i class="fa-solid fa-chevron-down"></i></h2>
                        <h4 id="section1" style="display: none;">AnimatedScoreboard es un plugin que sirve para agregar una scoreboard a los usuarios. AnimatedScoreboard tiene una característica para agregar una scoreboard para los usuario de unos rangos específicos. AnimatedScoreboard cuenta con un sistema de animaciones para que tú scoreboard se vea mejor. También se pueden agregar placeholders con el plugin placeholderapi estos son contenidos que interactuan con el jugador o con el servidor. AnimatedScoreboard cuenta con comandos para reiniciar la scoreboard y que se agregan las actualizaciones del plugin sin tener que actualizarla.</h4>
                    </div>
                    <div class="section">
                        <h2 class="btn-section" data-section="section2">Descripción de AnimatedScoreboard v2 configurado <i class="fa-solid fa-chevron-down"></i></h2>
                        <h4 id="section2" style="display: none;">AnimatedScoreboard v2 pre-configured cuenta con varios placeholders. De título tiene una preciosa animación con tonos claros y oscuros verdes. La scoreboard está repleta de emojis dentro de Minecraft (esos emojis no están puesto con un plugin). La scoreboard cuenta con 3 líneas informativas de esa forma la scoreboard se hace más compacta y más agradable para los jugadores de tu servidor de Minecraft. El contenido de la scoreboard se adapta automáticamente, eso significa que cada jugador ve en la scoreboard dependiendo de su información y estadísticas. Actualmente el idioma de la scoreboard es español, pero puedes modificar la scoreboard como tú quieras desde el archivo defaultscoreboard.yml.</h4>
                    </div>
                    <div class="section">
                        <h2 class="btn-section" data-section="section3">Como instalarlo <i class="fa-solid fa-chevron-down"></i></h2>
                        <h4 id="section3" style="display: none;">Descárgate el fichero y desarchive lo. Descargase el plugin e inicie el server. A continuación, diríjase a los ficheros de su servidor, entre a la carpeta con el nombre plugins y elimine la carpeta antigua de AnimatedScoreboard y agregue la nueva. Es su decisión descargar algunos plugins para poner más apis y que su scoreboard sea más dinámica y que tenga más utilidad es su servidor.</h4>
