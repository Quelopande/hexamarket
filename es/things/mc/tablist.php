                <div class="sections">
                    <div class="section">
                        <h2 class="btn-section" data-section="section1">¿Qué es Tablist? <i class="fa-solid fa-chevron-down"></i></h2>
                        <h4 id="section1" style="display: none;">Tablist es un plugin de Minecraft hecho para que los configurados de los servidores modifiquen los tabuladores del servidor, para que se vea mejor. Se pueden ocultar algunos jugadores y también sirve para que los rangos se vean. Este plugin tiene algunos placeholders para que la Tablist sea más útil.</h4>
                    </div>
                    <div class="section">
                        <h2 class="btn-section" data-section="section2">Descripción de Tablist configurado <i class="fa-solid fa-chevron-down"></i></h2>
                        <h4 id="section2" style="display: none;">Tablist es un plugin muy fácil de configurar, pero a veces el diseño no nos convence y lo tenemos que repetir una y otra vez hasta llegar a un diseño bonito y que no ocupe toda la pantalla para que así no estrese a los jugadores y no disminuya el rango de visión del jugador. Aquí podrás encontrar una Tablist con una animación y 2 placeholders. Tiene un diseño precioso y además esta no ocupa tanta parte de la pantalla para que los jugadores puedan ver bien lo que está pasando dentro del juego. Los rangos se ven gracias al plugin Nametagedit.</h4>
                    </div>
                    <div class="section">
                        <h2 class="btn-section" data-section="section3">Como instalarlo <i class="fa-solid fa-chevron-down"></i></h2>
                        <h4 id="section3" style="display: none;">Descargate el fichero y desarchivelo. Descargese el plugin e inicie el server. A continuación dirijase a los ficheros de su servidor, entre a la carpeta con el nombre plugins y elimine la carpeta antigua de Tablist y agregue la nueva. Es su decision descargar algunos plugins para poner mas apis y que su tablist sea más profesional, recuerde configurar el Nametagedit si es que tiene algun sistema de rangos puesto en su servidor</h4>
                    </div>
