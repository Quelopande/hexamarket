                    <div class="section">
                        <h2 class="btn-section" data-section="section1">¿Qué es economyshopgui? <i class="fa-solid fa-chevron-down"></i></h2>
                        <h4 id="section1" style="display: none;">Economyshopgui es un plugin de Minecraft que sirve principalmente para crear una tienda dentro de el juego para comprar objetos con el dinero de el juego.</h4>  
                    </div>
                    <div class="section">
                        <h2 class="btn-section" data-section="section2">Descripción de economyshopgui configurado <i class="fa-solid fa-chevron-down"></i></h2>
                        <h4 id="section2" style="display: none;">Por desgracia todos los configuradores de Minecraft sabemos lo difícil que es configurar el plugin economyshopgui ya que los tutoriales que hay por fotos de internet no están bien explicados debido a lo complejo que es configurarlo. Cambiar los precios de venta y de compra es lo más difícil de configurar. Los precios tan baratos y con un alto precio de venta son algunas de las cosas que vienen por predeterminado son algunas de las cosas que complican la tarea de configurar a la mayoría de los configurados. Aquí te traemos Economyshopgui pre-configured, se trata de el economyshopgui reordenada y con los precios bien regulados para que tu servidor tenga una economía muy buena, y que tú server tenga un ambiento más profesional y más equilibrado.</h4>
                    </div>
                    <div class="section">
                        <h2 class="btn-section" data-section="section3">Como instalarlo <i class="fa-solid fa-chevron-down"></i></h2>
                        <h4 id="section3" style="display: none;">Descargarte el plugin economyshopgui (link abajo), instale Vault (link abajo) e instale su sistema de economía y configúrelo (Essentials X no requiere de configuración).  Vaya a los archivos de su servidor, ingrese a la carpeta de plugins y ponga todos los archivos que se ha descargado antes ahí. Inicie el server, diríjase denuevo a la carpeta plugins y elimine la carpeta antigua de economyshopgui. Desarchive el archivo .zip. Coloque la nueva carpeta dentro de la sección de plugins. Inicie el servidor y verifique que todo esté bien.</h4>
                    </div>
