                    <div class="section">
                        <h2 class="btn-section" data-section="section1">¿Qué es CommandPanels? <i class="fa-solid fa-chevron-down"></i></h2>
                        <h4 id="section1" style="display: none;">CommandPanels es un plugin de Minecraft que permite crear inventarios interactivos personalizados. Puedes usar variables, datos y marcadores de posición para personalizar tus GUIs. Los inventarios pueden estar animados y cambiar ítems constantemente. ¡Incluso puedes hacer una tienda con este plugin!</h4>
                    </div>
                    <div class="section">
                        <h2 class="btn-section" data-section="section2">Descripción de CommandPanels configurado <i class="fa-solid fa-chevron-down"></i></h2>
                        <h4 id="section2" style="display: none;">CommandPanels configured tiene 4 menús llenos de colores y de logotipos, cosas y objetos que lo hace más bonito. La mayoría de los elementos en los menús son ToolTips (Al pasar el ratón por encima muestra información). Pero también hay otros elementos que te redirigirán a otro menú, pero también tu al hacer clic a algunos objetos específicos te enseña en el chat del servidor mensajes informativos sobre el servidor, estos solo se le mostrarán a la persona que lo ha clicado, te recomendamos que a estos elementos informativos les cambies la información. Hay un menú de estadísticas, una de kits que está hecho para ejecutar el comando /kit {nombre de el kit} de essentialsX. ¡Pero esto no es todo! también hay un menú que te muestra las estadísticas del jugador que abrió el menú.</h4>
                    </div>
                    <div class="section">
                        <h2 class="btn-section" data-section="section3">Como instalarlo <i class="fa-solid fa-chevron-down"></i></h2>
                        <h4 id="section3" style="display: none;">Descárgate el fichero y des archívelo. Descárguese el plugin Luckperms, póngalo en la carpeta plugins de su servidor e inicie el server. A continuación, diríjase a los ficheros de su servidor, entre a la carpeta con el nombre plugins y elimine la carpeta antigua de Luckperms y agregue lo que obtuvo anteriormente después de desarchivar el fichero. Es su decisión decidir si poner algunos como PlaceholderApi para que los menús funcionen correctamente. También le recomendamos que se ponga EssentialsX o algún tipo de plugin de kits en su servidor para que el menú de kits funcione, pero antes debe de cambiar el contenido del menú en el archivo dentro de la carpeta "servidor/plugins/Panels" y vaya a kits.yml para editar el comando de este menú. Le recomendamos que haga lo mismo con los mensajes del menu principal (el archivo se llama "panel-1.yml").</h4>
                    </div>
