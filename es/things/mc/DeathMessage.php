                    <div class="section">
                        <h2 class="btn-section" data-section="section1">¿Qué es DeathMessage? <i class="fa-solid fa-chevron-down"></i></h2>
                        <h4 id="section1" style="display: none;">DeathMessage es un simple plugin que sirve para informar a los usuarios de cómo han muerto ellos u otro usuario. Este plugin se suele poner con una finalidad estética, informativa o simplemente para poner un mensaje gracioso para las muertes. También se puede usar para desinformar, es decir que alguien puede poner que alguien ha muerto en lava y eso se mostrará, pero el usuario se murió de otra manera. Este plugin también sirve para dar la bienvenida a usuarios nuevos, recurrentes u otros. Y para dar la despedida a los usuarios que se fueron del servidor porque querían o porque un administrador lo expulsó.</h4>
                    </div>
                    <div class="section">
                        <h2 class="btn-section" data-section="section2">Descripción de DeathMessage configurado <i class="fa-solid fa-chevron-down"></i></h2>
                        <h4 id="section2" style="display: none;">DeathMessage configured es el mismo plugin de siempre, pero customizado con una finalidad estética e informativa.  Los mensajes están con gradientes con colores que describen la muerte del usuario (ej. Mueres de frio: el color va de blanco a azul). También los mensajes de bienvenida están customizados estos al tu entrar lo primero que aparece en el mensaje es [+] o [-] dependiendo si has entrado o salido. El nombre del usuario parece en amarillo para resaltar el nombre del resto del mensaje de color gris.</h4>
                    </div>
                    <div class="section">
                        <h2 class="btn-section" data-section="section3">Como instalarlo <i class="fa-solid fa-chevron-down"></i></h2>
                        <h4 id="section3" style="display: none;">Descarga el fichero de DeathMessage en el enlace que te dejamos abajo. El fichero ponlo en el apartado plugins. Después inicia el servidor. Cuando ya este iniciado del todo apágalo y ve a la carpeta de plugins en tu servidor y borra la carpeta. Acto seguido ve y descarga el fichero dándole a el botón de descarga que está debajo. Desarchiva el archivo Zip que te da y la carpeta que obtienes ponla en la carpeta plugins y inicia el servidor denuevo para revisar que todo esté correcto.</h4>
                    </div>
